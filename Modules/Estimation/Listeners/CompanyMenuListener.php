<?php

namespace Modules\Estimation\Listeners;

use App\Events\CompanyMenuEvent;

class CompanyMenuListener
{
    /**
     * Handle the event.
     */
    public function handle(CompanyMenuEvent $event): void
    {
        $module = 'Estimation';
        $menu = $event->menu;
        $menu->add([
            'title' => 'Estimation',
            'icon' => 'home',
            'name' => 'estimation',
            'parent' => null,
            'order' => 2,
            'ignore_if' => [],
            'depend_on' => [],
            'route' => 'home',
            'module' => $module,
            'permission' => 'manage-dashboard'
        ]);
    }
}
